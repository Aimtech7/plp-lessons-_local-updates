fruits = ["apple", "banana", "mango", "orange", "grape"]
for i in fruits:
    print(i)
    
    
# for loop with range
for i in range(5):
    print(i)
    
for i in range(2, 10, 2):
    print(i)    
    
# nested loop
for i in range(3):
    for j in range(2):
        print(i, j) 
# while loop
i = 0
while i < 5:
    print(i)
    i += 1  
    